package com.github.xaeka.javalearn.chapter04.numeral;

/**
 * <p>十六进制转二进制</p>
 * <ul>
 *   <li>规则：将十六进制数每1位，转成对应的4位的一个二进制数即可。</li>
 * </ul>
 *
 * @author Xaeka
 * @version 1.0
 * @since 2025-11-20 15:56:47
 */

public class HexadecimalToBinary {
    public static void main(String[] args) {
        // 将 0x23B 转为二进制
        System.out.println(0x23B);
        System.out.println(0b001000111011);
        System.out.println(571);
    }
}

