/**
 * <p>switch分支 基本语法</p>
 * <ul>
 *   <li>1. switch关键字，表示switch分支</li>
 *   <li>2. 表达式 对应一个值（是一个具体的值, 只要返回一个变量值的，都可以被称为表达式）</li>
 *   <li>3. case 常量1：当表达式的值等于常量1，就执行语句块1</li>
 *   <li>4. break：表示退出switch分支</li>
 *   <li>5. 如果与 case常量1 匹配，就执行语句块1，如果没有匹配，就自上而下匹配case常量2、case常量3……</li>
 *   <li>6. 如果一个都没有匹配上，那么就执行default</li>
 * </ul>
 *
 * @author Xaeka
 * @version 1.0
 * @since 2025/12/4 14:12
 */

