package com.github.xaeka.javalearn.chapter04.operator;

/**
 * <p>位运算符</p>
 * <ul>
 *   <li>java中有7个位运算（&、I、^、~、>>、<<和>>>)</li>
 *   <li>分别是按位与&、按位或！、按位异或，按位取反～，它们的运算规则是：</li>
 *   <li>按位与   &    两位全为1，结果为1，否则为0</li>
 *   <li>按位或   |    两位有一个为1，结果为1，否则为0</li>
 *   <li>按位异或 ^    两位一个为0，一个为1，结果为1，否则为0</li>
 *   <li>按位取反 ~   0->1,1->0</li>
 *   <li>比如：2&3=？ ~-2=？~2=？  2|3=?   2^3=？</li>
 * </ul>
 *
 * @author Xaeka
 * @version 1.0
 * @since 2025-11-20 17:15:11
 */

public class BitwiseOperator {
    public static void main(String[] args) {
        // 2&3=?    按位与
        // 2  原码  => 00000000 00000000 00000000 00000010    反码  =>  00000000 00000000 00000000 00000010     补码  =>  00000000 00000000 00000000 00000010
        // 3  原码  => 00000000 00000000 00000000 00000011    反码  =>  00000000 00000000 00000000 00000011     补码  =>  00000000 00000000 00000000 00000011
        // 2 & 3 按位与：
        // 2的补码： 00000000 00000000 00000000 00000010
        // 3的补码： 00000000 00000000 00000000 00000011
        // 按位与：  00000000 00000000 00000000 00000010
        // 即为 2
        System.out.println(2 & 3);  // 2
        System.out.println(0b00000010); // 2

        // ~-2=?    取反
        // -2  原码  => 10000000 00000000 00000000 00000010    反码  =>  11111111 11111111 11111111 11111101     补码  =>  11111111 11111111 11111111 11111110
        // ~-2 取反  => 00000000 00000000 00000000 00000001
        System.out.println(~-2);    //  1
        System.out.println(0b00000001); //  1



        // ~2=?     取反
        // 2  原码  => 00000000 00000000 00000000 00000010    反码  =>  00000000 00000000 00000000 00000010     补码  =>  00000000 00000000 00000000 00000010
        // ~2 取反  => 11111111 11111111 11111111 11111101(此时还是补码，不是我们需要的源原码！！！！要取反加一变为原码！！！)
        // ~2 补码取反  => 10000000 00000000 00000000 00000010 再加1 => 10000000 00000000 00000000 00000011   即为-3
        System.out.println(~2); // -3
        System.out.println(-0b00000011); // -3


        // 2|3=?    按位或
        // 2  原码  => 00000000 00000000 00000000 00000010    反码  =>  00000000 00000000 00000000 00000010     补码  =>  00000000 00000000 00000000 00000010
        // 3  原码  => 00000000 00000000 00000000 00000011    反码  =>  00000000 00000000 00000000 00000011     补码  =>  00000000 00000000 00000000 00000011
        // 2|3 按位或=>00000000 00000000 00000000 00000011
        System.out.println(2|3);    // 3
        System.out.println(0b00000011); // 3

        // 2^3=?    按位异或
        // 2  原码  => 00000000 00000000 00000000 00000010    反码  =>  00000000 00000000 00000000 00000010     补码  =>  00000000 00000000 00000000 00000010
        // 3  原码  => 00000000 00000000 00000000 00000011    反码  =>  00000000 00000000 00000000 00000011     补码  =>  00000000 00000000 00000000 00000011
        // 2^3 按位异或=>00000000 00000000 00000000 00000001
        System.out.println(2^3);    // 1
        System.out.println(0b00000001); // 1


    }
}

