public class BinaryToExercise {
    public static void main(String[] args) {
        // 课堂练习：
        // 1. 请将 0b11100101 转成八进制
        System.out.println(0b11100101);
        System.out.println(0345);
        System.out.println(229);

        // 2. 请将 0b1110010110 转成十六进制
        System.out.println(0b1110010110);
        System.out.println(0x396);
        System.out.println(918);

    }
}

