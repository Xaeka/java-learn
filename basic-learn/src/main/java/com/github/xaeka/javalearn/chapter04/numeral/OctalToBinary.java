package com.github.xaeka.javalearn.chapter04.numeral;

/**
 * <p>八进制转二进制</p>
 * <ul>
 *   <li>规则：将八进制数每1位，转成对应的一个3位的二进制数即可。</li>
 * </ul>
 *
 * @author Xaeka
 * @version 1.0
 * @since 2025-11-20 15:49:49
 */

public class OctalToBinary {
    public static void main(String[] args) {
        // 将 0237 转为 二进制
        System.out.println(0237);
        System.out.println(0b010011111);
        System.out.println(159);
    }
}
