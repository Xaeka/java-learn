/**
 * <p>各类常用进制转二进制</p>
 *
 * @author Xaeka
 * @version 1.0
 * @since 2025-11-20 15:59:56
 */

public class ToBinaryExercise {
    public static void main(String[] args) {
        // 1. 01230 转成二进制
        System.out.println(01230);
        System.out.println(0b01010011000);
        System.out.println(664);
        // 2. 0xAB29 转成二进制
        System.out.println(0xAB29);
        System.out.println(0b1010101100101001);
        System.out.println(43817);
    }
}

