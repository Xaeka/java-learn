package com.github.xaeka.javalearn.chapter03.homework;

/**
 * <p>第三章 - 课后练习题02</p>
 * <ul>
 *   <li>使用char类型，分别保存 \n \t \r \\ 1 2 3 等字符。并打印输出</li>
 * </ul>
 *
 * @author Xaeka
 * @version 1.0
 * @since 2025-11-13 17:59:55
 */

public class Homework02 {
    public static void main(String[] args) {
        // 换行
        char c1 = '\n';
        System.out.println(c1);
        // 制表
        char c2 = '\t';
        System.out.println(c2);
        // 回车
        char c3 = '\r';
        System.out.println(c3);
        // 斜杠
        char c4 = '\\';
        System.out.println(c4);
        // 字符 1
        char c5 = '1';
        System.out.println(c5);
        // 字符 2
        char c6 = '2';
        System.out.println(c6);
        // 字符 3
        char c7 = '3';
        System.out.println(c7);
    }
}


