public class Var02 {
    public static void main(String[] args) {
        // 记录信息
        int age = 21;
        double score = 98.5;
        char gender = '男';
        String name = "Xaeka";
        // 输出
        System.out.println("人的信息如下：");
        System.out.println("名字" + name);
        System.out.println("年龄" + age);
        System.out.println("评分" + score);
        System.out.println("性别" + gender);
    }
}
